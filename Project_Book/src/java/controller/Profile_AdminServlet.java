    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        HttpSession session = request.getSession();
        Account a = (Account) session.getAttribute("account");
        DAOAccount dal = new DAOAccount();
        Admin ad = dal.getAdmin(a.getUsername());
        request.setAttribute("admin", ad);
        request.setAttribute("profile", "profile");
        request.getRequestDispatcher("profile_admin.jsp").forward(request, response);
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        int adminId = Integer.parseInt(request.getParameter("adminId"));
        String username = request.getParameter("username");
        String firstname = request.getParameter("firstname");
        String lastname = request.getParameter("lastname");
        String email = request.getParameter("email");
        String phone = request.getParameter("phone");
        String address = request.getParameter("address");
        String image = request.getParameter("image");
        DAOAccount dal = new DAOAccount();
        Admin ad = dal.getAdmin(username);
        if(image==null || "".equals(image))
            image = ad.getImage();
        else
            image = "images/avatar/"+image;
        dal.updateAdmin(adminId, username, firstname, lastname, email, phone, address, image);
        ad = dal.getAdmin(username);
        request.setAttribute("admin", ad);
        request.setAttribute("profile", "profile");
        request.setAttribute("updateSuccsess", "Cập nhật thành công");
        request.getRequestDispatcher("profile_admin.jsp").forward(request, response);
    }

